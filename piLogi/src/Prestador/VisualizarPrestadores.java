    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
       String nomePesquisar = txtPesquisar.getText().trim();

try {
    CadastroPrestadoresDAO objDAO = new CadastroPrestadoresDAO();
    DefaultTableModel model = (DefaultTableModel) tabelaPrestadores.getModel();
    model.setNumRows(0);

    ArrayList<CadastroPrestadoresDTO> lista;

    if (nomePesquisar.isEmpty()) {
        // Mostra todos os clientes
        lista = objDAO.PesquisarPrestadores(); // Método que busca todos
    } else {
        // Filtra pelo nome digitado
        lista = objDAO.PesquisarPrestadoresPorNome(nomePesquisar);
    }

    for (CadastroPrestadoresDTO Prestadores : lista) {
        model.addRow(new Object[]{
            Prestadores.getIdPrestadores(),
            Prestadores.getNome(),
            Prestadores.getTelefone(),
            Prestadores.getCnpj(),
            Prestadores.getEmail(),
            Prestadores.getEndereco(),
            Prestadores.getRamo(),
            Prestadores.getSenha()
        });
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Erro ao pesquisar: " + e.getMessage());
}

        
    }//GEN-LAST:event_pesquisarActionPerformed

    private void Mudar_telaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Mudar_telaActionPerformed
                                       
    int setar = tabelaPrestadores.getSelectedRow();
    
    if (setar >= 0) {
        
        String id = tabelaPrestadores.getModel().getValueAt(setar, 0).toString();
        String nome = tabelaPrestadores.getModel().getValueAt(setar, 1).toString();
        String telefone = tabelaPrestadores.getModel().getValueAt(setar, 2).toString();
        String cnpj = tabelaPrestadores.getModel().getValueAt(setar, 3).toString();
        String email = tabelaPrestadores.getModel().getValueAt(setar, 4).toString();
        String endereco = tabelaPrestadores.getModel().getValueAt(setar, 5).toString();
        String ramo = tabelaPrestadores.getModel().getValueAt(setar, 6).toString();
        String senha = tabelaPrestadores.getModel().getValueAt(setar, 7).toString();
        
        TelaEditarPrestadores cadastro = new TelaEditarPrestadores();
        cadastro.setDados(id, nome, telefone, cnpj, email, endereco, ramo, senha);
        cadastro.setVisible(true);
        this.setVisible(false); 
    } else {
        JOptionPane.showMessageDialog(null, "Selecione uma linha para carregar os dados.");
    }


    }//GEN-LAST:event_Mudar_telaActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
int linhaSelecionada = tabelaPrestadores.getSelectedRow();

    if (linhaSelecionada >= 0) {
        int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este Prestador?", "Confirmação", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
           
            int id = Integer.parseInt(tabelaPrestadores.getModel().getValueAt(linhaSelecionada, 0).toString());

            try {
                CadastroPrestadoresDAO dao = new CadastroPrestadoresDAO();
                dao.excluirPrestadores(id);
                JOptionPane.showMessageDialog(null, "Prestador excluído com sucesso!");
                ListarValores(); 
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir prestador: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um prestador para excluir.");
    }


    }//GEN-LAST:event_ExcluirActionPerformed

