    private void ListarValores(){
    try {
        CadastroPrestadoresDAO objCadastroPrestadoresDAO = new CadastroPrestadoresDAO();
        
        DefaultTableModel model = (DefaultTableModel) tabelaPrestadores.getModel();
        model.setNumRows(0);
        
        ArrayList<CadastroPrestadoresDTO> lista = objCadastroPrestadoresDAO.PesquisarPrestadores();
        
        for (int num = 0; num < lista.size(); num++) {
            CadastroPrestadoresDTO prestador = lista.get(num);
            model.addRow(new Object[]{
                prestador.getIdPrestadores(),
                prestador.getNome(),
                prestador.getTelefone(),
                prestador.getCnpj(),
                prestador.getEmail(),
                prestador.getEndereco(),
                prestador.getRamo(),
                "*******", // Exibe a senha como asteriscos
            });
        }
        
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(null, "Listar valores VisualizarPrestadores: " + erro);
    }
}


  

    
