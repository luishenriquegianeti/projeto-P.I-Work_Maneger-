    private void VerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerificarActionPerformed
        try{
            String Email, Senha;

            Email = txtemail.getText();
            Senha = txtsenha.getText();

            CadastroClientesDTO objClientesdto = new CadastroClientesDTO();
            objClientesdto.setEmail(Email);
            objClientesdto.setSenha(Senha);

            CadastroClientesDAO objClientesdao = new CadastroClientesDAO();
            ResultSet rsclientedao = objClientesdao.autenticacaoUsuario(objClientesdto);

            if (rsclientedao.next()){
                HomePrestador objhome = new HomePrestador();
                objhome.setVisible(true);

                dispose();

            }else {
                JOptionPane.showMessageDialog(null, "Email ou senha invalidos");
            }

        }catch (SQLException erro ){
            JOptionPane.showMessageDialog(null,"login" + erro);
        }

    }//GEN-LAST:event_VerificarActionPerformed

